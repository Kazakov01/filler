#include "includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len_s;
	char	*find;

	len_s = ft_strlen(s);
	find = (char *)(s + len_s);
	len_s += 1;
	while (len_s--)
	{
		if (*find == (unsigned char)c)
			return (find);
		find--;
	}
	return (0);
}
